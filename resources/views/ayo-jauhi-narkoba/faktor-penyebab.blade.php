<h1 class="text-xl font-bold">Faktor Penyebab Penggunaan Narkoba </h1>
<img src="{{ asset('img/photorealistic-pills-tablets-different-colors-with-text-stop-drugs.jpg') }}" class="mx-auto mt-10"
    width="400px" alt="">
<div class="indent-8">
    <p class="text-base my-3">
        Menurut Libertus Jehani dan Antoro (2006), remaja menggunakan
        narkoba dapat disebabkan oleh faktor internal dan eksternal (Azzahra,
        Gilza dkk 2021). Faktor internal meliputi:
    </p>
    <ol class="space-y-3 list-decimal list-inside indent-8">
        <li> <span class="font-semibold text-lg">Kepribadian.</span> <br>Remaja dengan kepribadian yang
            tidak stabil (labil) lebih rentan untuk terjerumus dalam penggunaan narkoba.</li>

        <li> <span class="font-semibold text-lg">Keluarga.</span> <br>Remaja yang berasal dari keluarga
            yang tidak harmonis
            mungkin merasa putus asa dan frustrasi, sehingga mereka
            mencari pelarian melalui narkoba.</li>
        <li> <span class="font-semibold text-lg">Ekonomi.</span> <br>Remaja dari latar belakang ekonomi
            rendah yang kesulitan
            mencari pekerjaan mungkin tergoda untuk menjadi pengedar
            narkoba demi mendapatkan penghasilan cepat. Sebaliknya,
            remaja dari keluarga dengan ekonomi cukup yang kurang
            mendapatkan perhatian atau terpengaruh oleh kelompok
            pertemanan yang buruk juga rentan menjadi pengguna narkoba.</li>
    </ol>


    <p class="mb-3 mt-3"><span class="font-bold text-xl ">
            Faktor eksternal
        </span>
        adalah faktor dari luar yang dapat mempengaruhi
        remaja dalam mengambil keputusan untuk menggunakan narkoba,
        termasuk:</p>

    <ol class="space-y-3 list-decimal list-inside indent-8">
        <li> <span class="font-semibold text-lg">Pergaulan.</span> <br>Kelompok teman sebaya memiliki pengaruh kuat, di
            mana
            ajakan dari teman atau kelompok bisa mendorong remaja untuk
            menggunakan narkoba.</li>

        <li> <span class="font-semibold text-lg">Lingkungan Sosial atau Masyarakat.</span> <br>Lingkungan sosial yang
            baik dan terkontrol dapat mencegah
            peredaran narkoba. Namun, jika lingkungan sosial dan
            masyarakat apatis dan tidak peduli, hal ini bisa menyebabkan
            meningkatnya penggunaan narkoba di kalangan remaja.</li>

    </ol>
</div>
