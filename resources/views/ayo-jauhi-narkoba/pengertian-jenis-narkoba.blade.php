<h1 class=" text-xl mb-2 font-bold">Pengertian Narkoba </h1>
<p class="indent-8">
    Menurut Badan Narkotika Nasional Republik Indonesia atau BNN,
    Narkotika merujuk pada substansi atau obat, entah berasal dari alam,
    diproduksi secara sintetis, atau dihasilkan secara semi-sintetis, yang
    menghasilkan efek penurunan kesadaran dan halusinasi. Sementara
    berdasarkan UU Narkotika Pasal 1 ayat 1, narkotika adalah zat buatan
    atau alami yang dapat menyebabkan efek halusinasi, penurunan
    kesadaran, dan memicu kecanduan.
</p>

<h1 class="text-xl my-2 font-bold">Jenis-jenis Narkoba</h1>

<p class="indent-8">Penggolongan Narkoba di Indonesia berdasarkan Undang-undang
    Nomor 35 Tahun 2009 dan Peraturan Menteri Kesehatan. Narkoba
    terdiri dari tiga golongan: Narkotika, Psikotropika, dan Bahan Adiktif.</p>

<p class="indent-8">Narkotika adalah zat atau obat yang berasal dari tanaman atau
    bukan tanaman, baik sintetis maupun semi sintetis, yang dapat
    menyebabkan penurunan atau perubahan kesadaran, hilangnya rasa,
    mengurangi sampai menghilangkan rasa nyeri, dan dapat
    menimbulkan ketergantungan. </p>

<p class="indent-8 mt-6">Narkotika di Indonesia dikelompokkan menjadi tiga golongan
    berdasarkan potensi mengakibatkan ketergantungan:
</p>


