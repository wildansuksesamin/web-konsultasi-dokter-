berdasarkan potensi mengakibatkan ketergantungan: --}}
<ol class="mt-3 space-y-3 list-decimal list-inside indent-8">
    <li> <span class="font-semibold text-lg">
            Narkotika Golongan I.
        </span> <br>Narkotika golongan I sangat berbahaya
        dan tidak dapat digunakan untuk pengobatan karena berpotensi
        tinggi menyebabkan ketergantungan. Contoh narkotika
        golongan I adalah opium, ganja, heroin, amfetamin,
        metamfetamin, etkatinon, dan tanaman KHAT.</li>
    <li> <span class="font-semibold text-lg">Narkotika Golongan II.</span> <br> Narkotika golongan II dapat
        dimanfaatkan untuk pengobatan, tetapi tetap berpotensi
        menyebabkan ketergantungan. Contoh narkotika golongan II
        adalah dekstromoramid, metadon, morfin, petidin,
        dihidroetorfin, oripavin, dan lain-lain.</li>
    <li> <span class="font-semibold text-lg">Narkotika Golongan III.</span> <br>Narkotika golongan III dapat digunakan
        sebagai pengobatan dan memiliki potensi ketergantungan yang
        lebih ringan dibanding golongan I dan golongan II. Contoh
        narkotika golongan III adalah kodein, narkodein, buprenorfin,
        dan lain-lain. </li>
</ol>

<p class="indent-8 my-3"> <span class="text-xl font-bold">Psikotropika</span> adalah zat atau obat, baik alamiah
    maupun
    sintetis
    bukan narkotika, yang dapat menyebabkan perubahan pada fungsi
    mental dan emosional, seperti perasaan euforia maupun peningkatan
    energi. <br> Psikotropika di Indonesia dikelompokkan menjadi empat
    kategori berdasarkan potensi yang mengakibatkan ketergantungan:</p>

<ol class=" space-y-3 list-decimal list-inside indent-8">
    <li> <span class="font-semibold text-lg">Psikotropika Golongan I.</span> <br>Psikotropika golongan I adalah
        psikotropika yang hanya dapat digunakan untuk ilmu
        pengetahuan dan tidak digunakan dalam terapi, serta
        mempunyai potensi amat kuat mengakibatkan ketergantungan.
        Contoh: Deskloroketamin, Flualprazolam.</li>
    <li> <span class="font-semibold text-lg">Psikotropika Golongan II.</span> <br>Psikotropika yang dapat digunakan
        untuk pengobatan dan terapi, serta untuk tujuan ilmu
        pengetahuan, akan tetapi mempunyai potensi kuat
        mengakibatkan ketergantungan. Contoh: Metilfenidat,
        Sekobarbital.</li>
    <li> <span class="font-semibold text-lg">Psikotropika Golongan III.</span> <br>Psikotropika yang digunakan untuk
        pengobatan dan untuk tujuan ilmu pengetahuan, serta
        mempunyai potensi ketergantungan yang sedang. Contoh:
        Flunitrazepam, Pentobrabital, Pentazosin</li>
    <li> <span class="font-semibold text-lg">Psikotropika Golongan IV.</span> <br>
        Psikotropika yang digunakan untuk
        pengobatan dan untuk tujuan ilmu pengetahuan serta
        mempunyai potensi ketergantungan yang ringan. Contoh:
        Alprazolam, Lorazepam, Klobazam, Diazepam dan sebagainya.</li>
</ol>

<p class="my-3 indent-8">Secara umum, narkotika mengarah ke efek penghilang rasa sakit
    dan potensi kecanduan yang dirasakan setelah mengonsumsinya.
    Sedangkan psikotropika mengarah ke efek perubahan mental dan
    perilaku setelah mengonsumsinya. </p>
