<h1 class="text-xl font-bold">Bentuk Pencegahan Narkoba</h1>

<img src="{{ asset('assets/img/3978729.jpg') }}" class="rounded w-full h-72 mx-auto object-contain my-4" alt="">

<div class="text-justify indent-8">
    <p>Bahaya narkoba sangat serius dan penyebarannya di lingkungan
        sekitar sangat cepat, diperlukan upaya untuk mengatasinya. Berikut
        adalah beberapa cara untuk mencegah penyalahgunaan narkoba (Rosa,
        Dwi Yunisa 2023):</p>

    <ol class="list-decimal list-inside space-y-2 my-3">
        <li>
            <span class="italic font-semibold">Preventif (pencegahan)</span><br>
            <p class="indent-8">Membangun masyarakat yang memiliki ketahanan dan kekebalan terhadap narkoba. Pencegahan
                ini lebih baik daripada
                pemberantasan. Cara-cara pencegahan termasuk pembinaan,
                penyuluhan, serta pengawasan di masyarakat dan tempat
                lainnya, serta tindakan untuk mengurangi atau menghilangkan
                peluang penyalahgunaan narkoba</p>

        </li>
        <li>
            <span class="italic font-semibold">Represif (penindakan)
            </span><br>
            <p class="indent-8">Menindak dan memberantas penyalahgunaan narkoba
                melalui jalur hukum yang dilakukan oleh aparat hukum dan
                keamanan, dengan bantuan masyarakat. Masyarakat harus
                melaporkan penyalahgunaan narkoba kepada pihak berwajib
                dan tidak boleh main hakim sendiri.</p>
        </li>
        <li>
            <span class="italic font-semibold">Kuratif (pengobatan)
            </span><br>
            <p class="indent-8">Bertujuan menyembuhkan para pengguna, baik secara medis
                maupun dengan metode lain. Di negara ini sudah banyak tempat
                pengobatan dan rehabilitasi untuk pengguna narkoba, seperti
                tempat rehabilitasi atau yayasan yang menangani kasus narkoba.</p>
        </li>
        <li>
            <span class="italic font-semibold">Rehabilitatif (rehabilitasi)
            </span><br>
            <p class="indent-8">Dilakukan agar setelah pengobatan, para pecandu tidak
                kembali menggunakan narkoba. Rehabilitasi bertujuan
                memperlakukan para pengguna narkoba dengan wajar, agar
                mereka dapat kembali ke masyarakat dalam keadaan sehat
                secara fisik dan mental.</p>
        </li>
    </ol>

</div>
